class Calculator:

    def sum(self,a,b):
        print('Sum is :',a+b)

    def diff(self,a,b):
        print('Diff is :',a-b)

    def Mul(self,a,b):
        print('Mul is :',a*b)

    def div(self,a,b):
        print('Div is :',a/b)

cal1 = Calculator()
cal1.sum(20,5)
cal1.diff(20,5)
cal1.Mul(20,5)
cal1.div(20,5)